// A. Buatlah sebuah array berisi 5 buah-buahan (string).
const fruits = ['apple', 'banana', 'coconut', 'mango', 'orange'];
console.log(fruits);

// B. Lalu tambahkan satu buah.
fruits.push('grape');
console.log(fruits);

// C. Edit/ubah satu buah.
fruits.splice(2, 1, 'melon');
console.log(fruits);

// D. Kurangi satu buah terakhir dari array.
fruits.pop();
console.log(fruits);